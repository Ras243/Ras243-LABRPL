# Perulangan (for loop)

# ini dengan list
angka_1 = [0,1,4,8,10] #ini adalah list
print(angka_1)

for i in angka_1:
    print(f"i sekarang -> {i}")

print("saya baru pertama kali belajar perulangan(loop)\n")

# ini dengan range
angka_2 = range(1,5)

for i in angka_2:
    print(f"i sekarang -> {i}")

print("yok...yok..bisa yok\n")

# menggunakan string
data_str = "saya pasti bisa"
for huruf in data_str:
    print(huruf)
    
